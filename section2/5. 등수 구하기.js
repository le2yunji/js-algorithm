function solution(arr) {
  let answer = [];
  let score = [...arr]; // ê¹Šì€ ë³µì‚¬ë¡œ ì›ë³¸ ë°°ì—´ ë³´ì¡´í•˜ê¸°
  score.sort((a, b) => b - a); // sort() ë©”ì„œë“œëŠ” ë°°ì—´ì˜ ìš”ì†Œë¥¼ ì •ë ¬í•˜ì—¬ 'ì›ë³¸ ë°°ì—´ì„ ì§ì ‘ ë³€ê²½(mutate)'í•˜ê³ , ì •ë ¬ëœ ë°°ì—´ì„ ë°˜í™˜     [100, 92, 89, 87, 76]
  for (let x of arr) {
    for (let i = 0; i < score.length; i++) {
      if (x === score[i]) answer.push(i + 1);
    }
  }
  return answer;
}

